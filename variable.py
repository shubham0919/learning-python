# Variable = A conatiner for a value ( string , integer , float , boolean )
#            A variable behaves as it was the value it contains

# String
first_name = "Shubham"
last_name = "Sonwane"

# Integer
age = 23

# Float
height = 5.8

# Boolean
is_student = False

# Printing the variables
print(f"Hi I am {first_name} {last_name}")
print(f"I am {age} years old")
print(f"My height is {height}")
print(f"Am I a student ? {is_student}")